#ifndef __TYPEDEF_H
#define __TYPEDEF_H

typedef char s8;
typedef short s16;
typedef int  s32;
typedef long long s64;

typedef unsigned char u8;
typedef unsigned short u16;
typedef unsigned int u32;
typedef unsigned long long u64;

typedef unsigned char BOOL;

#if !defined(TRUE)
#define TRUE   1
#endif

#if !defined(FALSE)
#define FALSE   0
#endif

#endif //__TYPEDEF_H

