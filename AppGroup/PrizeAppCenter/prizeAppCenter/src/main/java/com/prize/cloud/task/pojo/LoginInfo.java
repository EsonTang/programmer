package com.prize.cloud.task.pojo;

/**
 * 个人信息json对象
 * @author yiyi
 * @version 1.0.0
 */
public class LoginInfo {

	private String realName, phone, passport, avatar, userId, email;
	private int gender;

	public String getRealName() {
		return realName;
	}

	public void setRealName(String realName) {
		this.realName = realName;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getAvatar() {
		return avatar;
	}

	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPassport() {
		return passport;
	}

	public void setPassport(String passport) {
		this.passport = passport;
	}

	public int getGender() {
		return gender;
	}

	public void setGender(int gender) {
		this.gender = gender;
	}

	@Override
	public String toString() {
		return "LoginInfo [realName=" + realName + ", phone=" + phone
				+ ", passport=" + passport + ", avatar=" + avatar + ", userId="
				+ userId + ", email=" + email + ", gender=" + gender + "]";
	}
	
	
}
